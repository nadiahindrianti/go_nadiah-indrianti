package calculate

func Addition(x, y int) int {
	result := x + y
	return result
}

func Subtraction(x, y int) int {
	result := x - y
	return result
}

func Divison(x, y int) int {
	result := x / y
	return result
}

func Multiplication(x, y int) int {
	result := x * y
	return result
}
